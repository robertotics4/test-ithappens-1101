    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        // Persistindo um novo Cliente
        ClienteDAO cDao = new ClienteDAO();
        Cliente cliente = new Cliente();
        Endereco end = new Endereco();

        cliente.setNome(tfNome.getText());
        end.setEndereco(tfEndereco.getText());
        end.setBairro(tfBairro.getText());
        end.setCidade(tfCidade.getText());
        end.setUf(cbUF.getSelectedItem().toString());
        end.setCep(ffCep.getText());
        cliente.setEndereco(end);
        cliente.setTelefone(ffTelefone.getText());

        // Se existir um cliente selecionado: ATUALIZA, senão: CADASTRA
        if (existeSelecionado) {
            cliente.setId(Long.parseLong(tfCodigo.getText()));
            cDao.update(cliente);
            JOptionPane.showMessageDialog(null, "Cliente atualizado com sucesso.");
        } else {
            cDao.persist(cliente);
            JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso.");
        }

        clearAll();
        loadTable();
    }//GEN-LAST:event_btAdicionarActionPerformed

