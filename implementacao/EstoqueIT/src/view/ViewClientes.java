    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // Evento para deletar um registro

        // Verificando se existe um registro selecionado
        if (tfCodigo.getText().equals("")) {
            return;
        } else {
            // Buscando e deletando o registro no banco
            ClienteDAO cDao = new ClienteDAO();
            Cliente cliente = cDao.findById(Long.parseLong(tfCodigo.getText()));

            if (cliente == null) {
                JOptionPane.showMessageDialog(null, "Cliente não encontrado.");
            } else {
                cDao.remove(cliente);
                JOptionPane.showMessageDialog(null, "O Cliente " + cliente.getNome() + " foi deletado com sucesso.");
                loadTable();
                clearAll();
            }
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void tbClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbClientesMouseClicked
        // Método para selecionar um registro da tabela e carregar os campos
        int linha = tbClientes.getSelectedRow();
        int coluna = tbClientes.getSelectedColumn();

        if (linha >= 0) {
            existeSelecionado = true;

            ClienteDAO cDao = new ClienteDAO();
            Cliente cliente = new Cliente();
            long idSelecionado = Long.parseLong(tbClientes.getValueAt(linha, 0).toString());

            cliente = cDao.findById(idSelecionado);

            // setando valores nos campos do formulário
            tfCodigo.setText(String.valueOf(cliente.getId()));
            tfNome.setText(cliente.getNome());
            tfEndereco.setText(cliente.getEndereco().getEndereco());
            tfBairro.setText(cliente.getEndereco().getBairro());
            tfCidade.setText(cliente.getEndereco().getCidade());
            cbUF.setSelectedItem(cliente.getEndereco().getUf());
            ffCep.setText(cliente.getEndereco().getCep());
            ffTelefone.setText(cliente.getTelefone());
        } else {
            existeSelecionado = false;
        }

        enableBtSalvar();
        enableBtExcluir();
    }//GEN-LAST:event_tbClientesMouseClicked

