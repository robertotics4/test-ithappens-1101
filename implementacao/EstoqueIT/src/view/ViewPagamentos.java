    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // Persistindo uma nova Forma de Pagamento
        FormaPagamentoDAO fpDao = new FormaPagamentoDAO();
        FormaPagamento formaPagamento = new FormaPagamento(tfDescricao.getText());

        // Se existir um registro selecionado: ATUALIZA, senão: CADASTRA
        if (existeSelecionado) {
            formaPagamento.setId(Long.parseLong(tfCodigo.getText()));
            fpDao.update(formaPagamento);
            JOptionPane.showMessageDialog(null, "Forma de Pagamento atualizada com sucesso.");
        } else {
            fpDao.persist(formaPagamento);
            JOptionPane.showMessageDialog(null, "Forma de Pagamento cadastrado com sucesso.");
        }

        clearAll();
        loadTable();
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // Evento para deletar um registro

        // Verificando se existe um registro selecionado
        if (!tfCodigo.getText().equals("")) {
            // Buscando e deletando o registro no banco
            FormaPagamentoDAO fpDao = new FormaPagamentoDAO();
            FormaPagamento formaPagamento = fpDao.findById(Long.parseLong(tfCodigo.getText()));

            if (formaPagamento == null) {
                JOptionPane.showMessageDialog(null, "Forma de pagamento não encontrada.");
            } else {
                fpDao.remove(formaPagamento);
                JOptionPane.showMessageDialog(null, "A Forma de Pagamento" + formaPagamento.getDescricao() + " foi deletada com sucesso.");
                loadTable();
                clearAll();
            }
        }
    }//GEN-LAST:event_btExcluirActionPerformed

