    private void tbFormaPagamentoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbFormaPagamentoMouseClicked
        // Método para selecionar um registro da tabela e carregar os campos
        int linha = tbFormaPagamento.getSelectedRow();
        int coluna = tbFormaPagamento.getSelectedColumn();

        if (linha >= 0) {
            existeSelecionado = true;

            FormaPagamentoDAO fpDao = new FormaPagamentoDAO();
            FormaPagamento formaPagamento = new FormaPagamento();
            long idSelecionado = Long.parseLong(tbFormaPagamento.getValueAt(linha, 0).toString());

            formaPagamento = fpDao.findById(idSelecionado);

            // setando valores nos campos do formulário
            tfCodigo.setText(String.valueOf(formaPagamento.getId()));
            tfDescricao.setText(formaPagamento.getDescricao());
        } else {
            existeSelecionado = false;
        }
    }//GEN-LAST:event_tbFormaPagamentoMouseClicked

